#include "3-calc.h"
/**
 * op_add - adds @a and @b
 * @a: 1
 * @b: 2
 * Return: sum of @a and @b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub- sub @a and @b
 * @a: 1
 * @b: 2
 * Return: sub of @a and @b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies @a and @b
 * @a: 1
 * @b: 2
 * Return: mul @a and @b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides @a and @b
 * @a: 1
 * @b: 2
 * Return: div @a and @b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - reminder of devi of @a and @b
 * @a: 1
 * @b: 2
 * Return: the reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
